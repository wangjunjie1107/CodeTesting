#include<iostream>
using namespace std;
 
int main()
{
	cout<<"输入一个数字：";
	int n;
	cin >> n;
	
	
	cout<<"n       n*n" << endl;
	cout<< n <<"    "<< n*n <<"  (十进制  decimal) "<<endl;
	
	
	//十六进制 
	cout<< hex;   //设置十六进制输出
	cout<< n <<"    "<< n*n <<"  (十六进制 hexadecimal) "<<endl;
	
	//八进制 
	oct(cout); //设置八进制输出 
	cout<< n <<"    "<< n*n <<"  (八进制 octal) "<<endl;
	
	return 0; 
 
 }
 
 
 
 
 
 
 
 
 
 
 
 
  
